 <h2> About Online Grievance Redressal System </h2>

Grievance Redressal System is a mechanism commonly used to receive and act on complaints or grievances reported by stakeholders of private or public institutions, enabling prompt actions on any issue raised by them and to avail services more effectively.

Unlike the traditional Grievance Redressal approach through letters and complaint forms, the new online based approach such as grievance portals have very great appeal and its usage highly reflects the actual state of customer satisfaction.

Grievance Redressal can be handled directly by institutes through their own websites. Also the smart web portal for grievance processing connects stakeholders and action-takers directly through online platform. Grievance Redressal System helps to pursue quick action for solving the grievance, while maintaining affordability and ease to the users. Online grievance redressal mechanism in higher education institutions facilitates the main constituents of the institute including students, parents, teachers and other non-teaching staff to lodge grievance and ensure satisfactory solutions in a hassle-free manner. Effective and timely redressal or settlement of the grievances makes Grievance Redressal System a mandatory mechanism for higher education institution, according to the Ministry of Human Resource Development (MHRD), Government of India

